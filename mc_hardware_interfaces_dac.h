#pragma once

#include <stdint.h>

#ifdef __cplusplus

class DacBase {
public:
	//**********************************************************************
	// Метод должен:
	// 1. Отключить DAC.
	// 2. Сконфигурировать DAC и его канал в заданном режиме.
	// 3. Включить его.
	//**********************************************************************
	virtual bool	reinit			( void )			const = 0;

	//**********************************************************************
	// Выставление заданного значения на канал.
	//**********************************************************************
	virtual void	setValue		( const uint32_t ch, const uint32_t value )	const = 0;
};

#endif
